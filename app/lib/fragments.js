const MENU_FRAGMENT = `#graphql
  fragment MenuItem on MenuItem {
    id
    resourceId
    tags
    title
    type
    url
  }
  fragment ChildMenuItem on MenuItem {
    ...MenuItem
  }
  fragment ParentMenuItem on MenuItem {
    ...MenuItem
    items {
      ...ChildMenuItem
    }
  }
  fragment Menu on Menu {
    id
    items {
      ...ParentMenuItem
    }
  }
`;

export const HEADER_QUERY = `#graphql
  fragment Shop on Shop {
    id
    name
    description
    primaryDomain {
      url
    }
    brand {
      logo {
        image {
          url
        }
      }
    }
  }
  query Header(
    $country: CountryCode
    $headerMenuHandle: String!
    $language: LanguageCode
  ) @inContext(language: $language, country: $country) {
    shop {
      ...Shop
    }
    menu(handle: $headerMenuHandle) {
      ...Menu
    }
  }
  ${MENU_FRAGMENT}
`;

// Fragment for fetching Shopify page content
export const PAGE_FRAGMENT = `#graphql
  fragment Page on Page {
    id
    title
    handle
    body
    bodySummary
    seo {
      description
      title
    }
    createdAt
    updatedAt
  }
`;

// Query for fetching a single page by handle
export const PAGE_QUERY = `#graphql
  query Page(
    $language: LanguageCode
    $country: CountryCode
    $handle: String!
  ) @inContext(language: $language, country: $country) {
    page(handle: $handle) {
      ...Page
    }
  }
  ${PAGE_FRAGMENT}
`;

// Query for fetching multiple pages by handles
export const PAGES_QUERY = `#graphql
  query Pages(
    $language: LanguageCode
    $country: CountryCode
    $query: String!
  ) @inContext(language: $language, country: $country) {
    pages(first: 50, query: $query) {
      nodes {
        ...Page
      }
    }
  }
  ${PAGE_FRAGMENT}
`;

